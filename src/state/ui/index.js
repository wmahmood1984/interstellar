export const balance = createAsyncThunk("balance",
    async ({contract,address})=>{

		
        try {
            const balance1 = await contract.methods.balanceOf(address).call()
			const Active = await contract.methods.TradingOpen().call()
			const Price = await contract.methods.preSalePrice().call()
			const MaxMint = await contract.methods.maxBuy().call()

			const remaining = MaxMint - balance1

             return {balance1,Active,Price,remaining}

        } catch (error) {
            console.log("Error in ArrayThunk",error)
        }
    }
    )


